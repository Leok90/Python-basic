# ListEx07_김세용
# 리스트 뒤집기(reverse)
a = ['a', 'c', 'b']
a.reverse()
print(a)
print("-" * 15)

# 위치 반환(index)
a = ['a', 'c', 'b']
print(a.index('b'))
print("-" * 15)

# 리스트에 요소 삽입(insert)
# 파이썬에서는 숫자를 0부터 센다
a = [1, 2, 3]
a.insert(0, 4)
print(a)
a.insert(3, 5)
print(a)
print("-" * 15)